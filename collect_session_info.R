# Data Directory

This directory contains manifests only. The public repository does not redistribute large public datasets, controlled data, or laboratory source files.

Use:

- `external_data_manifest.csv` to identify the public source and expected local path for each input dataset.
- `expected_intermediate_outputs.csv` to identify generated files, their producer notebook, and their downstream role.

Before running notebooks, download or generate the required data into the paths listed in the manifests.
